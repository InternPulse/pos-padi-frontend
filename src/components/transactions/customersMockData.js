function transformCustomerData(customer) {
  return {
    id: customer.id,
    imageURL: null,
    firstName: customer.first_name,
    lastName: customer.last_name,
    email: customer.email || "unavailable",
    phone: customer.phone,
    dateCreated: customer.created_at,
    customerId: customer.customer_id,
    performanceSummary: [],
  };
}

export function transformCustomers(arr){

  let customersData
  let customersDataList

  const initialState =   [{
    id: '',
    imageURL: null,
    firstName: "",
    lastName: "",
    email: "",
    phone: "",
    dateCreated: "",
    customerId: "",
    performanceSummary: [],
  }]

  if(arr.length > 0){
    customersData = arr.map(item => transformCustomerData(item))
   
  }else{
    customersData = []
  }

  
  if(arr.length > 0){
    customersDataList = listCustomers(customersData)
    
  }else{
    customersDataList = listCustomers(initialState)
  }

  return {
    rawCustomers: customersData,
    customersList: customersDataList
  }


}
